    <script>
        // PIN Visibility Toggle - Login
        document.querySelector('.toggle-pin-visibility')?.addEventListener('click', function(e) {
            e.preventDefault();
            const pinInput = document.getElementById('pin');
            const icon = this.querySelector('.material-symbols-outlined');
            if (pinInput.type === 'password') {
                pinInput.type = 'text';
                icon.textContent = 'visibility_off';
            } else {
                pinInput.type = 'password';
                icon.textContent = 'visibility';
            }
        });
        
        // PIN Visibility Toggle - Register
        document.querySelector('.toggle-reg-pin-visibility')?.addEventListener('click', function(e) {
            e.preventDefault();
            const pinInput = document.getElementById('new-pin');
            const icon = this.querySelector('.material-symbols-outlined');
            if (pinInput.type === 'password') {
                pinInput.type = 'text';
                icon.textContent = 'visibility_off';
            } else {
                pinInput.type = 'password';
                icon.textContent = 'visibility';
            }
        });
        
        // PIN Confirmation Visibility Toggle
        document.querySelector('.toggle-reg-pin-confirm-visibility')?.addEventListener('click', function(e) {
            e.preventDefault();
            const pinInput = document.getElementById('pin-confirm');
            const icon = this.querySelector('.material-symbols-outlined');
            if (pinInput.type === 'password') {
                pinInput.type = 'text';
                icon.textContent = 'visibility_off';
            } else {
                pinInput.type = 'password';
                icon.textContent = 'visibility';
            }
        });
        
        // Modal Functions
        function openRegisterModal() {
            document.getElementById('registerModal').classList.remove('hidden');
        }
        
        function closeRegisterModal() {
            document.getElementById('registerModal').classList.add('hidden');
        }
        
        // Close modal when clicking outside
        document.getElementById('registerModal')?.addEventListener('click', function(e) {
            if (e.target === this) {
                closeRegisterModal();
            }
        });
        
        // Handle Registration Form Submit
        document.getElementById('registerForm')?.addEventListener('submit', function(e) {
            e.preventDefault();
            
            const formData = new FormData(this);
            
            fetch('{{ route("register") }}', {
                method: 'POST',
                headers: {
                    'X-Requested-With': 'XMLHttpRequest',
                    'Accept': 'application/json',
                },
                body: formData
            })
            .then(response => response.json())
            .then(data => {
                if (data.success) {
                    alert('Akun berhasil dibuat! Anda akan diarahkan ke halaman utama.');
                    window.location.href = data.redirect;
                } else {
                    displayRegisterErrors(data.errors || data.message);
                }
            })
            .catch(error => {
                console.error('Error:', error);
                displayRegisterErrors('Terjadi kesalahan saat mendaftar. Mohon coba lagi beberapa saat.');
            });
        });
        
        function displayRegisterErrors(errors) {
            const errorsDiv = document.getElementById('registerErrors');
            errorsDiv.classList.remove('hidden');
            
            if (typeof errors === 'string') {
                errorsDiv.innerHTML = '<p class="text-error font-label text-sm">' + errors + '</p>';
            } else {
                let html = '';
                for (const field in errors) {
                    if (Array.isArray(errors[field])) {
                        errors[field].forEach(msg => {
                            html += '<p class="text-error font-label text-sm">' + msg + '</p>';
                        });
                    } else {
                        html += '<p class="text-error font-label text-sm">' + errors[field] + '</p>';
                    }
                }
                errorsDiv.innerHTML = html;
            }
        }
